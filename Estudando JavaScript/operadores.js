// Os operadores são símbolos que realizam operações em um ou mais operandos. Eles podem ser classificados em diferentes tipos, como aritméticos, de comparação, lógicos, entre outros. Aqui estão alguns exemplos de operadores comuns:

// Operadores Aritméticos
var a = 10;
var b = 5;

console.log("Soma: " + (a + b)); // Soma: 15
console.log("Subtração: " + (a - b)); // Subtração: 5
console.log("Multiplicação: " + (a * b)); // Multiplicação: 50
console.log("Divisão: " + (a / b)); // Divisão: 2
console.log("Módulo: " + (a % b)); // Módulo: 0

// Operadores de Comparação
console.log("Igual a: " + (a == b)); // Igual a
console.log("Diferente de: " + (a != b)); // Diferente de
console.log("Maior que: " + (a > b)); // Maior que
console.log("Menor que: " + (a < b)); // Menor que
console.log("Maior ou igual a: " + (a >= b)); // Maior ou igual a
console.log("Menor ou igual a: " + (a <= b)); // Menor ou igual a

// Operadores Lógicos
var x = true;
var y = false;

console.log("E lógico: " + (x && y)); // E lógico
console.log("Ou lógico: " + (x || y)); // Ou lógico
console.log("Negação lógica de x: " + (!x)); // Negação lógica de x
console.log("Negação lógica de y: " + (!y)); // Negação lógica de y

// Operadores de Atribuição
var c = 10;

c += 5; // c = c + 5
console.log("Atribuição de adição: " + c); // Atribuição de adição: 15

c -= 3; // c = c - 3
console.log("Atribuição de subtração: " + c); // Atribuição de subtração: 12

c *= 2; // c = c * 2
console.log("Atribuição de multiplicação: " + c); // Atribuição de multiplicação: 24

c /= 4; // c = c / 4
console.log("Atribuição de divisão: " + c); // Atribuição de divisão: 6

c %= 5; // c = c % 5
console.log("Atribuição de módulo: " + c); // Atribuição de módulo: 1

// Resumo dos operadores em JavaScript:
// - Operadores Aritméticos: +, -, *, /, %
// - Operadores de Comparação: ==, !=, >, <, >=, <=
// - Operadores Lógicos: &&, ||, !
// - Operadores de Atribuição: =, +=, -=, *=, /=, %=